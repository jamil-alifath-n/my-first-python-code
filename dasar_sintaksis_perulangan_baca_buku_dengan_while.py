"""
Program perulangan membaca buku
"""

jumlah_buku = 10
print('Ibu memberi perintah : "Baca Buku!"')
jumlah_buku_yg_sudah_dibaca = 0

while jumlah_buku_yg_sudah_dibaca < jumlah_buku:
    jumlah_buku_yg_sudah_dibaca = jumlah_buku_yg_sudah_dibaca + 1
    print(f'Buku yang sudah dibaca {jumlah_buku_yg_sudah_dibaca} buku')

print(f"Jumlah buku yang sudah dibaca {jumlah_buku_yg_sudah_dibaca} buku")
